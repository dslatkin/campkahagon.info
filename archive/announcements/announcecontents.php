<h2>September 5th 2008:</h2>
<h3> (1) What became of Camp Kahagon's LAND?</h3>
<div>
<span>
Years ago I searched to find out what had happened to the camp property, and contacted various Bucks County and Quakertown officials.  I finally found someone and was informed that what was once Camp Kahagon is now included in "Nockamixon State Park":  www.Nockamixonsp@state.pa.us 
<br>It seems that just around the time Kahagon closed (1966), the state of PA was starting to buy up land for this state park.  Unfortunately, all the employees from that time period are long retired or gone, so the current ones only know this fact, but don't have any further info.  
If someone remembers our Camp's "official" address... the contact person I found at Nockamixon  (John T. Graham
Park Manager  Nockamixon State Park  215-529-7300) once said he'd mark the location on a park map and send it my way... if he's still there.  And, if anyone's interested then they could visit and check out the park's woods.  He said that he didn't think anything was left, but maybe some of the buildings' foundations might be there?!!<br /><br />
To see more info about Nockamixon State Park and where Camp Kahagon was a part of it, <a href="../memories/memorycontents.php?target=buck">click here</a>.

</span>
